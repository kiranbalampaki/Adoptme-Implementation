    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // dd(request()->all());
        $data = request()->validate([
            'title'    =>  'required',
            'story'     =>  'required',
            'story_image'    =>  'required|image|max:2048'
        ]);

        $image = $request->file('image');
        $new_name = rand() . '.' . $image->getClientOriginalExtension();
        $image->move(public_path('assets/uploads/storyimages'), $new_name);

        $story = new AdoptionStories();
        $story->title = $request->title;
        $story->story = $request->story;
        $story->story_image = $new_name;
        $story->user_id = $request->Auth::id();

        $story->save();
        return redirect()->back()->with('success','Story added successfully!');
    }

